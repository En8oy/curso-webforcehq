    {
        $user = User::where('slug', $slug)->with(
            [
                'posts' => function($q){
                    $q->select('id', 'user_id', 'category_id', 'slug', 'title', 'short', 'created_at');
                },
                'posts.category',
                'posts.user'
            ]
        )->first();

        return view('user.show', ['user' => $user]);
    }

